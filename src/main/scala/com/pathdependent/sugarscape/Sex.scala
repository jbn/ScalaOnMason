package com.pathdependent.sugarscape

trait DifferentSexes extends Agent {
  val sex: Sex
}

sealed abstract class Sex{
  def isMale(): Boolean
  def isFemale() = !isMale()
}
case object Male extends Sex{ def isMale() = true }
case object Female extends Sex{ def isMale() = false }

